export enum Ascension {

  /**
   * Ascension phase 0 when level in [1, 20]
   */
  ZERO,

  /**
   * Ascension phase 1 when level in [20, 40]
   */
  ONE,

  /**
   * Ascension phase 2 when level in [40, 50]
   */
  TWO,

  /**
   * Ascension phase 3 when level in [50, 60]
   */
  THREE,

  /**
   * Ascension phase 4 when level in [60, 70]
   */
  FOUR,

  /**
   * Ascension phase 5 when level in [70, 80]
   */
  FIVE,

  /**
   * Ascension phase 6 when level in [80, 90]
   */
  SIX
}
